public class Carta {

    String nome;
    String descricao;
    int ataque;
    int vida;
    int custo;
    int efeito;
    int idCarta;

    public Carta(String nome, String descricao, int ataque, int vida, int custo, int efeito, int idCarta) {
        this.nome = nome;
        this.descricao = descricao;
        this.ataque = ataque;
        this.vida = vida;
        this.custo = custo;
        this.efeito = efeito;
        this.idCarta = idCarta;
    }

}
